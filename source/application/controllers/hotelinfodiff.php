class Hotelinfodiff extends CI_Controller {
	//-------------------------------------------------------------------------------------------------------------------
	// @  コンストラクターメソッド
	//-------------------------------------------------------------------------------------------------------------------
	public function __construct() {
		parent::__construct ();
		$this->load->library ("xmlrpc");
		$this->load->model("Hotelinfodiff_model");
		$this->initialSettup();
	}
 
	//-------------------------------------------------------------------------------------------------------------------
	// @  プログラム起動修理
	//-------------------------------------------------------------------------------------------------------------------
	public function index(){
		$this->Hotelinfodiff_model->run();		
	}
	
	//-------------------------------------------------------------------------------------------------------------------
	// @  プログラム起動修理
	//-------------------------------------------------------------------------------------------------------------------
	public function initialSettup(){
		ini_set('memory_limit', '4096M');
		set_time_limit(0);
	}	
}
