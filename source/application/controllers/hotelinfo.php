class Hotelinfo extends CI_Controller {
	//-------------------------------------------------------------------------------------------------------------------
	// @  コンストラクターメソッド
	//-------------------------------------------------------------------------------------------------------------------
	public function __construct() {
		parent::__construct ();
		$this->load->library ("xmlrpc");
		$this->load->model("Hotelinfo_model");
		$this->initialSettup();
	}
 
	//-------------------------------------------------------------------------------------------------------------------
	// @概要  　　    プログラム起動修理
	// @param 1 　3わりのうち1分割でバッチ処理
	// @param 2 　3わりのうち2分割でバッチ処理
	// @param 3 　3わりのうち3分割でバッチ処理
	//-------------------------------------------------------------------------------------------------------------------
	public function index(){
		$type = 'clear';
		if($this->uri->segment(3) && ($this->uri->segment(3)=='group' || $this->uri->segment(3)=='all' )){
			if($this->uri->segment(4)){
				$type = $this->uri->segment(4);
			}
		}
		$this->Hotelinfo_model->run($type);
	}
	
	//-------------------------------------------------------------------------------------------------------------------
	// @  プログラム起動修理
	//-------------------------------------------------------------------------------------------------------------------
	public function initialSettup(){
		ini_set('memory_limit', '10240M');
		set_time_limit(0);
	}	
}
