<h1 style="font-size: 16px; margin-bottom: 20px;">宿泊約款</h1>
<dl>
  <dt style="padding: 1px 0pt; font-size: 14px; font-weight: bold; margin-bottom: 5px;">■
適用範囲</dt>
  <dd style="font-size: 12px;">第１条　当ホテルが宿泊客との間で締結する宿泊約款及びこれに関連する契約は、この約款
の定めるところによるものとし、この約款に定めのない事項については、法令又は一般に確立された慣習によるものとします。</dd>
  <dd style="font-size: 12px;">２．当ホテルが、法令及び慣習に反しない範囲で特約に応じたときは、前項の規定にかかわ
らず、その特約が優先するものとします。</dd>
</dl>
<br>
<dl>
  <dt style="padding: 1px 0pt; font-size: 14px; font-weight: bold; margin-bottom: 5px;">■
宿泊契約の申し込み</dt>
  <dd style="font-size: 12px;">第２条　当ホテルに宿泊約款の申込みをしようとする者は、次の事項を当ホテルに申し出て
いただきます。</dd>
  <dd style="margin: 0px 30px; font-size: 12px;">
    <ol>
      <li>宿泊者名</li>
      <li>宿泊日及び到着予定時刻</li>
      <li>宿泊料金（原則として別表第１の基本宿泊料による。）</li>
      <li>その他当ホテルが必要と認める事項</li>
    </ol>
  </dd>
  <dd style="font-size: 12px;">２．宿泊者が、宿泊中に前項第２号の宿泊日を超えて宿泊の継続を申し入れた場合、当ホテ
ルは、その申し出がなされた時点で新たな宿泊契約の申込みがあったものとして処理します。</dd>
</dl>
<br>
<dl>
  <dt style="padding: 1px 0pt; font-size: 14px; font-weight: bold; margin-bottom: 5px;">■
宿泊契約の成立等</dt>
  <dd style="font-size: 12px;">第３条　宿泊契約は、当ホテルが前条の申込みを承諾したときに成立するものとします。た
だし、当ホテルが承諾をしなかったことを証明したときは、この限りではありません。</dd>
  <dd style="font-size: 12px;">２．前項の規定により宿泊約款が成立したときは、宿泊期間（３日を超えるときは３日間）
の基本宿泊料を限度として当ホテルが定める申込金を、当ホテルが指定する日までに、お支払いいただきます。</dd>
  <dd style="font-size: 12px;">３．申込金は、まず、宿泊客が最終的に支払うべき宿泊料金に充当し、第６条及び第18条
の規定を適用する事態が生じたときは、違約金に次いで賠償金の順序で充当し、残額があれば、第12条の規定による料金の支払いの際に返還します。</dd>
  <dd style="font-size: 12px;">４．第２項の申込金を同項の規定により当ホテルが指定した日までにお支払いいただけない
場合は、宿泊約款はその効力を失うものとします。ただし、申込金の支払期日を指定するに当たり、当ホテルがその旨を宿泊客に告知した場合に限ります。</dd>
</dl>
<br>
<dl>
  <dt style="padding: 1px 0pt; font-size: 14px; font-weight: bold; margin-bottom: 5px;">■
申込金の支払いを要しないこととする特約</dt>
  <dd style="font-size: 12px;">第４条　前条第２項の規定にかかわらず、当ホテルは、契約の成立後同項の申込金の支払い
を要しないこととする特約に応じることがあります。</dd>
  <dd style="font-size: 12px;">２．宿泊契約の申込みを承諾するに当たり、当ホテルが前条第２項の申込金の支払いを求め
なかった場合及び当該申込金の支払い期日を指定しなかった場合は、前項の特約に応じたものとして取り扱います。</dd>
</dl>
<br>
<dl>
  <dt style="padding: 1px 0pt; font-size: 14px; font-weight: bold; margin-bottom: 5px;">■
宿泊契約締結の拒否</dt>
  <dd style="font-size: 12px;">第５条　当ホテルは、次に掲げる場合において、宿泊契約の締結に応じないことがありま
す。</dd>
  <dd style="margin: 0px 30px; font-size: 12px;">
    <ol>
      <li>宿泊の申込みが、この約款によらないとき。</li>
      <li>満室により客室の余裕がないとき。</li>
      <li>宿泊の申し込みをしようとする者が、宿泊に関し、法令の規定、公の秩序若しくは善良の風俗に反する行為をするおそれがあると認められ
るとき。</li>
      <li>宿泊の申し込みをしようとする者が、「暴力団員による不当な行為の防止等に関する法律」（平成４年３月１日施行）による暴力団および
その構成員ならびにその関係者、その他の反社会勢力であると認められるとき。</li>
      <li>宿泊の申し込みをしようとする者が暴力団又は暴力団員が事業活動を支配する法人その他の団体であるとき。</li>
      <li>宿泊の申し込みをしようとする者が、法人でその役員のうちに暴力団員に該当するものがいるもの。</li>
      <li>宿泊の申し込みをしようとする者が、宿泊に関してまたは当ホテル内で、暴行、脅迫、恐喝、不当な要求、賭博行為、使用禁止薬物の所持
もしくは使用、他の利用客に著しく迷惑を及ぼす行為、その他法令公序良俗に反する行為をするおそれがあるとき。</li>
      <li>宿泊の申し込みをしようとする者が、伝染病であると明らかに認められるとき。</li>
      <li>宿泊に関し合理的な範囲を超える負担を求められたとき。</li>
      <li>天災、施設の故障、その他やむを得ない事由により宿泊させることができないとき。</li>
      <li>宿泊しようとする者が、他の宿泊者に著しく迷惑を及ぼすおそれがあるとき、及び、宿泊者に著しく迷惑を及ぼす言動をしたとき。</li>
    </ol>
  </dd>
</dl>
<br>
<dl>
  <dt style="padding: 1px 0pt; font-size: 14px; font-weight: bold; margin-bottom: 5px;">■
宿泊客の契約解除権</dt>
  <dd style="font-size: 12px;">第６条　宿泊客は、当ホテルに申し出て、宿泊契約を解除することができます。</dd>
  <dd style="font-size: 12px;">２．当ホテルは、宿泊客がその責めに帰すべき事由により宿泊契約の全部又は、一部を解除
した場合（第３条第２項の規定により当ホテルが申込金の支払期日を指定してその支払いを求めた場合であって、その支払いより前に宿泊客が宿泊約款を解除し
たときを除きます。）は、別表第２に掲げるところにより、違約金を申し受けます。ただし、当ホテルが第４条第１項の特約に応じた場合であっては、その特約
に応じるに当たって、宿泊客が宿泊契約を解除したときの違約金支払義務について、当ホテルが宿泊客に告知したときに限ります。</dd>
  <dd style="font-size: 12px;">３．当ホテルは、宿泊客が連絡しないで宿泊当日の到着予定時刻になっても到着しないとき
は、その宿泊契約は宿泊客により解除されたものとみなし処理することがあります。</dd>
</dl>
<br>
<dl>
  <dt style="padding: 1px 0pt; font-size: 14px; font-weight: bold; margin-bottom: 5px;">■
当ホテルの契約解除権</dt>
  <dd style="font-size: 12px;">第７条　当ホテルは、次に掲げる場合においては、宿泊契約を解除することがあります。</dd>
  <dd style="margin: 0px 30px; font-size: 12px;">
    <ol>
      <li>宿泊客が、「暴力団員による不当な行為の防止等に関する法律」（平成４年３月１日施行）による暴力団およびその構成員ならびにその関
係者、その他の反社会勢力であると認められるとき。</li>
      <li>宿泊客が、宿泊に関してまたは当ホテル内で、暴行、脅迫、恐喝、不当な要求、賭博行為、使用禁止薬物の所持もしくは使用、他の利用客
に著しく迷惑を及ぼす行為、その他法令公序良俗に反する行為をするおそれがあるとき。</li>
      <li>宿泊客が伝染病であるとき、又はその疑いが濃厚なとき。</li>
      <li>宿泊に関し合理的な範囲を超える負担を求められたとき。</li>
      <li>天災等不可抗力に起因する事由により宿泊させることができないとき。</li>
      <li>宿泊客が、他の宿泊者に著しく迷惑を及ぼすおそれがあると認められるとき、及び、宿泊客が他の宿泊者に著しく迷惑を及ぼす言動をした
とき。</li>
      <li>寝室での寝たばこ、消防用設備等に対するいたずら、その他当ホテルが定める利用規則の禁止事項（火災予防上必要なものに限る。）に従
わないとき。</li>
    </ol>
  </dd>
  <dd style="font-size: 12px;">２．当ホテルが前項の規定に基づいて宿泊契約を解除したときは、宿泊者がいまだに提供を
受けていない宿泊サービス等の料金はいただきません。</dd>
</dl>
<br>
<dl>
  <dt style="padding: 1px 0pt; font-size: 14px; font-weight: bold; margin-bottom: 5px;">■
宿泊の登録</dt>
  <dd style="font-size: 12px;">第８条　宿泊者は、宿泊日当日、当ホテルのフロントにおいて、次の事項を登録していただ
きます。</dd>
  <dd style="margin: 0px 30px; font-size: 12px;">
    <ol>
      <li>宿泊者の氏名、年令、性別、住所及び職業。</li>
      <li>日本国内に住所登録地のない外国人にあっては、国籍、旅券番号、入国地及び入国年月日。（確認の為、パスポートのコピーをとらせてい
ただきます。）</li>
      <li>出発日及び出発予定時刻。</li>
      <li>その他当ホテルが必要と認める事項。</li>
    </ol>
  </dd>
  <dd style="font-size: 12px;">２．宿泊客が第12条の料金の支払いを宿泊券、クレジットカード等通貨に代わり得る方法
により行おうとするときは、あらかじめ、前項の登録時にそれらを呈示していただきます。</dd>
</dl>
<br>
<dl>
  <dt style="padding: 1px 0pt; font-size: 14px; font-weight: bold; margin-bottom: 5px;">■
客室の使用時間</dt>
  <dd style="font-size: 12px;">第９条　宿泊客が当ホテルの客室を使用できる時間は、午後4：00から翌午前10：00
までとします。</dd>
  <dd style="font-size: 12px;">２．当ホテルは、前項の規定にかかわらず、同項に定める時間外の客室の使用に応じること
があります。この場合には次に掲げる追加料金を申し受けます。</dd>
  <dd style="margin: 0px 30px; font-size: 12px;">
    <ol>
      <li>午後２時迄は、１時間1,000円(税込)。</li>
      <li>午後２時以降は、室料金の100％。</li>
    </ol>
  </dd>
</dl>
<br>
<dl>
  <dt style="padding: 1px 0pt; font-size: 14px; font-weight: bold; margin-bottom: 5px;">■
利用規則の遵守</dt>
  <dd style="font-size: 12px;">第１０条　宿泊客は、当ホテル内においては、当ホテルが定めてホテル内に掲示した利用規
則に従っていただきます。</dd>
</dl>
<br>
<dl>
  <dt style="padding: 1px 0pt; font-size: 14px; font-weight: bold; margin-bottom: 5px;">■
営業時間</dt>
  <dd style="font-size: 12px;">第１１条　当ホテルの主な施設等の営業時間は原則として次のとおりとし、その他の施設等
の詳しい営業時間は備付けのパンフレット、各所の掲示、客室内のサービス説明書等でご案内いたします。</dd>
  <dd style="margin: 0px 30px; font-size: 12px;">
    <ol>
      <li>フロントサービス時間</li>
      <ul>
        <li>門限（ロビー階正面玄関）　なし</li>
        <li>フロント　　　　　　　　　　 24時間</li>
      </ul>
    </ol>
  </dd>
  <dd style="font-size: 12px;">２．前項の時間は、必要な場合には変更することがあります。その場合には、適当な方法を
もってお知らせします。</dd>
</dl>
<br>
<dl>
  <dt style="padding: 1px 0pt; font-size: 14px; font-weight: bold; margin-bottom: 5px;">■
料金の支払い</dt>
  <dd style="font-size: 12px;">第１２条　宿泊客が支払うべき宿泊料金等の内訳及びその算定方法は、別表第１に掲げると
ころによります。</dd>
  <dd style="font-size: 12px;">２．前項の宿泊料金等の支払いは、通貨又は当ホテルが認めた宿泊券、クレジットカード等
これに代わり得る方法により、宿泊客の到着の際、フロントにおいて行っていただきます。</dd>
  <dd style="font-size: 12px;">３．当ホテルが宿泊客に客室を提供し、使用が可能になったのち、宿泊客が任意に宿泊しな
かった場合においても、宿泊料金は申し受けます。</dd>
</dl>
<br>
<dl>
  <dt style="padding: 1px 0pt; font-size: 14px; font-weight: bold; margin-bottom: 5px;">■
当ホテルの責任</dt>
  <dd style="font-size: 12px;">第１３条　当ホテルは、宿泊約款及びこれに関連する契約の履行に当たり、又はそれらの不
履行により宿泊客に損害を与えたときは、その損害を賠償します。ただし、それが当ホテルの責めに帰すべき事由によるものでないときは、この限りではありま
せん。</dd>
</dl>
<br>
<dl>
  <dt style="padding: 1px 0pt; font-size: 14px; font-weight: bold; margin-bottom: 5px;">■
契約した客室が提供できないときの取扱い</dt>
  <dd style="font-size: 12px;">第１４条　当ホテルは、宿泊客に契約した客室を提供できないときは、宿泊客の了解を得
て、できる限り同一の条件による他の宿泊施設を斡旋するものとします。</dd>
</dl>
<br>
<dl>
  <dt style="padding: 1px 0pt; font-size: 14px; font-weight: bold; margin-bottom: 5px;">■
寄託物等の取扱い</dt>
  <dd style="font-size: 12px;">第１５条　宿泊客がフロントにお預けになった物品又は現金並びに貴重品について、滅失、
毀損等の損害が生じたときは、それが、不可抗力である場合を除き、当ホテルは、その損害を賠償します。ただし、現金及び貴重品については、宿泊客がその種
類及び価額の明告を行わなかったときは、当ホテルは５万円を限度としてその損害を賠償します。</dd>
  <dd style="font-size: 12px;">２．宿泊客が当ホテル内にお持込みになった物品又は現金並びに、貴重品について、フロン
トにお預けにならなかったものに関しては当ホテルの故意又は重大な過失がない限り、滅失、毀損等の損害が生じても当ホテルは責任を負いかねます。</dd>
</dl>
<br>
<dl>
  <dt style="padding: 1px 0pt; font-size: 14px; font-weight: bold; margin-bottom: 5px;">■
宿泊客の手荷物又は携帯品の保管</dt>
  <dd style="font-size: 12px;">第１６条　宿泊客の手荷物が、宿泊に先立って当ホテルに到着した場合は、その到着前に当
ホテルが了解したときに限って責任をもって保管し、宿泊客がフロントにおいてチェックインする際にお渡しします。</dd>
  <dd style="font-size: 12px;">２．宿泊客がチェックアウトしたのち、宿泊客の手荷物又は携帯品が当ホテルに置き忘れら
れていた場合は、所有者の指示がない場合又は所有者が判明しないときは、発見日を含めて14日間当ホテルにて保管し、その後貴重品については最寄りの警察
署へ届け、その他の物品については処分させていただきます。</dd>
  <dd style="font-size: 12px;">（飲食物・雑誌に関しては即日処分とさせていただきます。）</dd>
</dl>
<br>
<dl>
  <dt style="padding: 1px 0pt; font-size: 14px; font-weight: bold; margin-bottom: 5px;">■
駐車の責任</dt>
  <dd style="font-size: 12px;">第１７条　宿泊客が当ホテルの駐車場をご利用になる場合、車両のキーの寄託の如何にかか
わらず、当ホテルは場所をお貸しするものであって、車両の管理責任まで負うものではありません。ただし、駐車場の管理に当たり、当ホテルの故意又は過失に
よって損害を与えたときは、その賠償の責めに任じます。</dd>
</dl>
<br>
<dl>
  <dt style="padding: 1px 0pt; font-size: 14px; font-weight: bold; margin-bottom: 5px;">■
宿泊客の責任</dt>
  <dd style="font-size: 12px;">第１８条　宿泊客の故意又は過失により当ホテルが損害を被ったときは、当該宿泊客は当ホ
テルに対し、その損害を賠償していただきます。</dd>
  <br>
</dl>
